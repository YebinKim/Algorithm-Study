#include <iostream>
using namespace std;

int main(int argc, const char * argv[]) {
    int n1, n2;
    
    cin >> n1 >> n2;
    
    cout << n1 + n2 << endl;
    cout << n1 - n2 << endl;
    cout << n1 * n2 << endl;
    cout << n1 / n2 << endl;
    cout << n1 % n2 << endl;
    
    return 0;
}
